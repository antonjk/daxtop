package com.dax.daxtop.config;

import java.io.Serializable;

/**
 * @author Dax Booysen
 */
public final class ConfigEntity implements Serializable
{
    /** The name of this entity */
    public String EntityName    = null;
    /** The value of this entity */
    public Object EntityValue   = null;
    
    /** Default constructor */
    public ConfigEntity() { }

    /** Value Constructor */
    public ConfigEntity(String Name, Object Value)
    {
        EntityName = Name;
        EntityValue = Value;
    }
}
